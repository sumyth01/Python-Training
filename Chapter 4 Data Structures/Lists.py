

# Lists are ordered, mutable collections of items.


# list_name = [item1, item2, item3]



# Creating a list
fruits = ["apple", "banana", "cherry"]

# Accessing elements
print(fruits[0])  # Output: apple

# Adding elements
fruits.append("orange")

# Removing elements
fruits.remove("banana")

# Looping through a list
for fruit in fruits:
    print(fruit)

